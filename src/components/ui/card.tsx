import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => {
  // color variant classes (use CSS variables / utility classes defined in index.css)
  const variants = [
    "card-variant-1",
    "card-variant-2",
    "card-variant-3",
    "card-variant-4",
  ];

  const variant = React.useMemo(() => {
    // if caller already provided a variant class, respect it
    if (className && /card-variant-\d/.test(className)) return "";
    return variants[Math.floor(Math.random() * variants.length)];
  }, [className]);

  return (
    <div
      ref={ref}
      className={cn(
        "rounded-xl border bg-card text-card-foreground shadow transition-all duration-300 transform-gpu hover:shadow-2xl hover:scale-105",
        variant,
        className
      )}
      {...props}
    />
  );
})
Card.displayName = "Card"

